public class floydstriangle {
    public static void main(String[] args) {
        int n=15;
        int a=1;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=n;j++){
                System.out.print(j);
                a++;
            }
            System.out.println();
        }
        
    }
    
}
